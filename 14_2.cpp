#include <stdio.h>
#include <stdlib.h>

int main() {
    int n;

    // Запитуємо користувача про розмір масиву
    printf("Введіть розмір масиву: ");
    scanf("%d", &n);

    // Виділяємо пам'ять для масиву
    int* arr = (int*)malloc(n * sizeof(int));

    // Запитуємо користувача ввести елементи масиву
    printf("Введіть елементи масиву:\n");
    for (int i = 0; i < n; i++) {
        printf("Елемент %d: ", i + 1);
        scanf("%d", &arr[i]);
    }

    // Використовуємо вказівники для обчислення суми елементів
    int* ptr = arr;
    int sum = 0;
    for (int i = 0; i < n; i++) {
        sum += *ptr;
        ptr++;  // Збільшуємо вказівник для переходу до наступного елементу
    }

    // Обчислюємо середнє значення
    double average = (double)sum / n;

    // Виводимо результат
    printf("\nСереднє значення елементів масиву: %.2f\n", average);

    // Звільняємо виділену пам'ять
    free(arr);

    return 0;
}
